package com.example.projecthoroscope.Model;

public class ModelHoroscope {
    private String id;
    private String nama_horoscope;
    private String old;
    private String now;
    private String deskripsi;
    private String photo;
    public String getId() {
        return id;
    }
    public void setId(String id) {
        this.id = id;
    }
    public String getNama_horoscope() {
        return nama_horoscope;
    }
    public void setNama_horoscope(String nama_horoscope) {
        this.nama_horoscope = nama_horoscope;
    }
    public String getOld() {
        return old;
    }
    public void setOld(String old) {
        this.old = old;
    }
    public String getNow() {
        return now;
    }
    public void setNow(String now) {
        this.now = now;
    }
    public String getDeskripsi() {
        return deskripsi;
    }
    public void setDeskripsi(String deskripsi) {
        this.deskripsi = deskripsi;
    }
    public String getPhoto() {
        return photo;
    }
    public void setPhoto(String photo) {
        this.photo = photo;
    }
}
